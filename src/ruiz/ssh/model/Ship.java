package ruiz.ssh.model;

public class Ship {
	private int id;
    private String name;
    private String seazone;
    private String nation;
    private String port;
    private String info;
    private String image;
    private int vcap;
    private int wcap;
    private int cost;
    private double speed;
    private double area;
    private double deckweight;
    private double drinkwater;
    private double drillwater;
    private double oil;
   
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSeazone() {
		return seazone;
	}
	public void setSeazone(String seazone) {
		this.seazone = seazone;
	}
	public String getNation() {
		return nation;
	}
	public void setNation(String nation) {
		this.nation = nation;
	}
	public String getPort() {
		return port;
	}
	public void setPort(String port) {
		this.port = port;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public int getVcap() {
		return vcap;
	}
	public void setVcap(int vcap) {
		this.vcap = vcap;
	}
	public int getWcap() {
		return wcap;
	}
	public void setWcap(int wcap) {
		this.wcap = wcap;
	}
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	public double getSpeed() {
		return speed;
	}
	public void setSpeed(double speed) {
		this.speed = speed;
	}
	public double getArea() {
		return area;
	}
	public void setArea(double area) {
		this.area = area;
	}
	public double getDeckweight() {
		return deckweight;
	}
	public void setDeckweight(double deckweight) {
		this.deckweight = deckweight;
	}
	public double getDrinkwater() {
		return drinkwater;
	}
	public void setDrinkwater(double drinkwater) {
		this.drinkwater = drinkwater;
	}
	public double getDrillwater() {
		return drillwater;
	}
	public void setDrillwater(double drillwater) {
		this.drillwater = drillwater;
	}
	public double getOil() {
		return oil;
	}
	public void setOil(double oil) {
		this.oil = oil;
	}
    
    
}
