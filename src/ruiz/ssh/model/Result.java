package ruiz.ssh.model;

public class Result {
	private int id;
    private int schid;
    private int shipid;
    private int order;
    private int baseid;
    private int loadlistid;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSchid() {
		return schid;
	}
	public void setSchid(int schid) {
		this.schid = schid;
	}
	public int getShipid() {
		return shipid;
	}
	public void setShipid(int shipid) {
		this.shipid = shipid;
	}
	public int getOrder() {
		return order;
	}
	public void setOrder(int order) {
		this.order = order;
	}
	public int getBaseid() {
		return baseid;
	}
	public void setBaseid(int baseid) {
		this.baseid = baseid;
	}
	public int getLoadlistid() {
		return loadlistid;
	}
	public void setLoadlistid(int loadlistid) {
		this.loadlistid = loadlistid;
	}
    
}
